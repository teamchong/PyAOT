# Test string operations

# Basic string
s = "hello world"
print(s)

# String concatenation
a = "hello"
b = "world"
print(a + " " + b)

# String length
print(len(s))

# String indexing
print(s[0])
print(s[4])

# String slicing
print(s[0:5])
print(s[6:])

# String comparison
print("abc" == "abc")
print("abc" != "def")
